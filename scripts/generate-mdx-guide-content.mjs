import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { basename, dirname, join } from "node:path";

const contentRoot = join(process.cwd(), "content", "guides");
const outputFile = join(process.cwd(), "src", "lib", "generated-mdx-guides.ts");

const entries = readdirSync(contentRoot)
  .filter((file) => file.endsWith(".mdx"))
  .sort((a, b) => a.localeCompare(b))
  .map((file) => {
    const slug = basename(file, ".mdx");
    const source = readFileSync(join(contentRoot, file), "utf8");
    return [slug, source];
  });

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(
  outputFile,
  [
    "// This file is generated by scripts/generate-mdx-guide-content.mjs.",
    "// Do not edit it by hand; edit content/guides/*.mdx instead.",
    "",
    "export const mdxGuideSources = {",
    ...entries.map(([slug, source]) => `  ${JSON.stringify(slug)}: ${JSON.stringify(source)},`),
    "} as const;",
    "",
    "export type MdxGuideSlug = keyof typeof mdxGuideSources;",
    "",
  ].join("\n"),
);

console.log(`Generated ${outputFile} with ${entries.length} guide(s).`);
